package com.iamsee.valuebean;

public class VoteSingle {

	private String  id;
	private String beselected;
	private String  votenum;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getBeselected() {
		return beselected;
	}
	public void setBeselected(String beselected) {
		this.beselected = beselected;
	}
	public String getVotenum() {
		return votenum;
	}
	public void setVotenum(String votenum) {
		this.votenum = votenum;
	}
	
	

	
}
